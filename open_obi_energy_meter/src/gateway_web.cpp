static void webTask(void *) {
  prefs.begin("obigw", true);
  prefs.getString("h", "").toCharArray(g_mqttHost, sizeof g_mqttHost);
  g_mqttPort = prefs.getUShort("p", 1883);
  prefs.getString("u", "").toCharArray(g_mqttUser, sizeof g_mqttUser);
  prefs.getString("pw", "").toCharArray(g_mqttPass, sizeof g_mqttPass);
  { String t = prefs.getString("t", "obi/gateway"); t.toCharArray(g_mqttTopic, sizeof g_mqttTopic); }
  g_mqttTls = prefs.getBool("tls", false);       // MQTTS on/off
  g_mqttCa  = prefs.getString("ca", "");         // optional broker CA cert (PEM)
  prefs.getString("au", "").toCharArray(g_authUser, sizeof g_authUser);   // web Basic Auth user (blank = open)
  prefs.getString("ap", "").toCharArray(g_authPass, sizeof g_authPass);
  { String z = prefs.getString("tz", "CET-1CEST,M3.5.0,M10.5.0/3"); z.toCharArray(g_tz, sizeof g_tz); }
  { String n = prefs.getString("ntp", "pool.ntp.org"); n.toCharArray(g_ntp, sizeof g_ntp); }
  g_nightMode = prefs.getBool("nightmode", false);
  g_priceCent = prefs.getFloat("pkwh", 31.0f);   // electricity price (€ ct/kWh) for the history cost columns
  prefs.end();
  setenv("TZ", g_tz, 1); tzset();                // apply the saved zone up-front (localtime works before NTP too)

  static char portBuf[8]; snprintf(portBuf, sizeof portBuf, "%u", g_mqttPort);
  pHost  = new WiFiManagerParameter("host", "MQTT host (blank = off)", g_mqttHost, sizeof g_mqttHost - 1);
  pPort  = new WiFiManagerParameter("port", "MQTT port", portBuf, 6);
  pUser  = new WiFiManagerParameter("user", "MQTT user", g_mqttUser, sizeof g_mqttUser - 1);
  pPass  = new WiFiManagerParameter("pass", "MQTT pass", g_mqttPass, sizeof g_mqttPass - 1);
  pTopic = new WiFiManagerParameter("topic", "MQTT base topic", g_mqttTopic, sizeof g_mqttTopic - 1);
  wm.addParameter(pHost); wm.addParameter(pPort); wm.addParameter(pUser);
  wm.addParameter(pPass); wm.addParameter(pTopic);
  wm.setConfigPortalBlocking(false);
  wm.setConfigPortalTimeout(0);
  wm.setEnableConfigPortal(false);   // never auto-launch the portal — it's on-demand (WiFi button / runPortal)
  wm.setCaptivePortalEnable(false);  // no DNS-hijack redirect -> the OS won't auto-pop a captive window
  wm.setBreakAfterConfig(true);
  Serial.printf("[web] WiFi: trying saved network (dashboard stays reachable either way, AP '%s')\n", apSsid());
  if (!wm.autoConnect(apSsid())) {   // try saved creds only; on failure bring up our own AP for the dashboard
    WiFi.mode(WIFI_AP_STA);
    WiFi.softAP(apSsid());
    g_apActive = true;
    Serial.printf("[web] no WiFi yet — dashboard on setup AP '%s' at http://192.168.4.1/\n", apSsid());
  }
  g_fsOk = mountHistoryFS();         // mount a spare data partition (userdata/spiffs/…) for the history log
  if (!g_fsOk) Serial.println("[web] history: no usable data partition — logging disabled");
  startServices();                   // dashboard is ALWAYS up (connected STA or the setup AP)
#ifdef OBI_HAS_EPD
  xTaskCreatePinnedToCore(epdTask, "epd", 4096, nullptr, 1, nullptr, 1);   // slow panel refresh off this loop
#endif

  for (;;) {
    if (g_portalReq) { g_portalReq = false; runPortal(); }   // WiFi button pressed -> open the WM portal
    // Watch WiFi transitions (e.g. after an in-dashboard connect): drop the setup AP once connected,
    // raise it again if WiFi is lost — so the dashboard is always reachable (STA IP or 192.168.4.1).
    bool conn = WiFi.status() == WL_CONNECTED;
    if (conn != g_wifiOk) {
      g_wifiOk = conn;
      if (conn) {
        if (g_apActive) { WiFi.softAPdisconnect(true); g_apActive = false; }
        applyMqttClient();
        syncNtp();  // wall clock for history day-buckets (user-set zone + NTP server)
        Serial.printf("[web] connected: http://%s/\n", WiFi.localIP().toString().c_str());
      } else if (!g_apActive) {
        WiFi.mode(WIFI_AP_STA); WiFi.softAP(apSsid()); g_apActive = true;
        Serial.printf("[web] WiFi lost — setup AP '%s' back up at 192.168.4.1\n", apSsid());
      }
    }
    if (g_serverUp) {
      server.handleClient();
      if (conn) mqttService();                                // keep MQTT serviced even during a reader OTA — pausing it
                                                              // let the broker's keepalive lapse and drop the client
                                                              // (which then didn't reconnect on its own)
      historyService();                                       // sample each reader's energy to LittleFS (throttled)
    }
    g_mqttUp = conn && mqtt.connected();                      // cache for the e-paper task (owns PubSubClient here)
    vTaskDelay(pdMS_TO_TICKS(2));
  }
}

