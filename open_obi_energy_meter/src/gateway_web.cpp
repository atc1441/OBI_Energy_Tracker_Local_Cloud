// Scan for WiFi networks (works while connected, in STA or AP+STA). Synchronous — pauses the web task a
// couple of seconds. Returns [{ssid,rssi,enc}]. Backs the dashboard WiFi panel's "Scan" button.
static void handleWifiScan() {
  int n = WiFi.scanNetworks();
  String j = "[";
  for (int i = 0; i < n; i++) {
    String s = WiFi.SSID(i);
    s.replace("\\", "\\\\"); s.replace("\"", "\\\"");
    if (i) j += ",";
    j += "{\"ssid\":\"" + s + "\",\"rssi\":" + String(WiFi.RSSI(i)) +
         ",\"enc\":" + String(WiFi.encryptionType(i) == WIFI_AUTH_OPEN ? 0 : 1) + "}";
  }
  j += "]";
  WiFi.scanDelete();
  server.send(200, "application/json", j);
}

// Connect to a network chosen in the dashboard (no AP switch needed when already connected). We answer
// BEFORE calling WiFi.begin(), because begin() drops the current link. Credentials persist to NVS, so
// autoConnect() uses them on the next boot. The webTask connect/disconnect watcher updates AP + state.
static void handleWifiConnect() {
  String ssid = server.arg("ssid"), pass = server.arg("pass");
  if (!ssid.length()) { server.send(400, "application/json", "{\"ok\":false,\"err\":\"ssid\"}"); return; }
  server.send(200, "application/json", "{\"ok\":true}");
  Serial.printf("[web] dashboard: connecting to '%s'\n", ssid.c_str());
  WiFi.persistent(true);
  if (WiFi.getMode() == WIFI_MODE_AP) WiFi.mode(WIFI_AP_STA);   // keep AP up during the switch
  WiFi.begin(ssid.c_str(), pass.c_str());
}

// Launch the WiFiManager config portal on demand (from webTask). Frees port 80 for WiFiManager, runs the
// portal (blocking, 3-min timeout) on the 'OpenOBI-<MAC>' AP, then brings our dashboard back up. Works
// both when unconfigured (already in AP mode) and when connected (lets you re-point WiFi/MQTT anytime).
static void runPortal() {
  Serial.printf("[web] opening WiFi config portal on AP '%s' (192.168.4.1)\n", apSsid());
  server.stop();
  g_serverUp = false;
  mqtt.disconnect();
  wm.setEnableConfigPortal(true);
  wm.setConfigPortalBlocking(true);
  wm.setConfigPortalTimeout(180);
  wm.startConfigPortal(apSsid());          // serves the WiFiManager UI until saved or 3-min timeout
  wm.setConfigPortalBlocking(false);
  wm.setConfigPortalTimeout(0);
  if (WiFi.status() != WL_CONNECTED) { WiFi.mode(WIFI_AP_STA); WiFi.softAP(apSsid()); g_apActive = true; }
  else g_apActive = false;                 // connected -> no setup AP needed
  startServices();                         // dashboard back up (STA IP or 192.168.4.1)
  Serial.println("[web] WiFi portal closed — dashboard restored");
}

// ---- MQTT command RX: publish "<seconds>" to <base>/<id>/set_interval ------
// e.g.  mosquitto_pub -t obi/gateway/238d4e/set_interval -m 30
// Per-gateway MQTT topics (namespaced by MAC so several gateways can share one base topic/broker):
//   <base>/gateway/<gwid>          -> gateway state JSON (temp, uptime, heap, rssi)
//   <base>/gateway/<gwid>/status   -> availability (LWT online/offline)
//   <base>/gateway/<gwid>/reboot   -> command topic (payload ignored) -> restart
static String gwStateTopic() { return String(g_mqttTopic) + "/gateway/" + gwId(); }
static String avtTopic()     { return gwStateTopic() + "/status"; }
static String rebootTopic()  { return gwStateTopic() + "/reboot"; }
static String authTopic()    { return gwStateTopic() + "/auth"; }

// Emit a wrong-web-login event on <base>/gateway/<gwid>/auth. Not retained — it's a point-in-time
// event (HA's `event` platform consumes it). Payload carries the attempted username, client IP and the
// running fail counter. Safe to call from the web handler: server.handleClient() and mqttService() both
// run on webTask, so there's no cross-task PubSubClient access.
static void publishAuthEvent(const String &user) {
  if (!mqtt.connected()) return;
  String p = "{\"event_type\":\"login_failed\",\"user\":" + jstr(user.c_str()) +
             ",\"ip\":\"" + server.client().remoteIP().toString() + "\"" +
             ",\"fail_count\":" + String(g_authFailCount) +
             ",\"uptime_s\":" + String(gw_uptime_s()) + "}";
  if (mqtt.publish(authTopic().c_str(), p.c_str())) g_mqttPubCount++;
}

static void mqttCallback(char *topic, byte *payload, unsigned int len) {
  String t(topic);
  if (t == rebootTopic()) { Serial.println("[mqtt] rx reboot command"); delay(150); ESP.restart(); }
  String base = String(g_mqttTopic) + "/";
  if (!t.startsWith(base)) return;
  String rest = t.substring(base.length());              // "<id>/set_interval"
  int slash = rest.indexOf('/');
  if (slash != 6) return;                                // id must be exactly 6 hex chars
  String id = rest.substring(0, slash), cmd = rest.substring(slash + 1);
  char buf[16]; unsigned n = len < sizeof(buf) - 1 ? len : sizeof(buf) - 1;
  memcpy(buf, payload, n); buf[n] = 0;
  long secs = atol(buf);
  if (cmd == "set_interval" && secs >= 1 && secs <= 65535) {
    uint8_t h[3];
    for (int i = 0; i < 3; i++) h[i] = (uint8_t)strtol(id.substring(i * 2, i * 2 + 2).c_str(), nullptr, 16);
    gw_request_interval(h, (uint16_t)secs);
    Serial.printf("[mqtt] rx set_interval %s -> %ld s\n", id.c_str(), secs);
  } else {
    Serial.printf("[mqtt] rx ignored topic=%s payload=%s\n", topic, buf);
  }
}

// ---- Home Assistant MQTT discovery ----------------------------------------
// One retained config per measurement on homeassistant/<component>/<node>/<obj>/config
// so HA auto-creates the entities and groups them under a device. The state stays the
// single JSON on <base>/<id>; each config just points its value_template at a JSON field.
// (The homeassistant/ discovery prefix is fixed here and is independent of the base topic.)
static void publishDiscovery(const Reader &r) {
  String id  = hex(r.handle, 3);                       // "e1c6c5"
  String uid = "obi_" + id;                            // device id / unique_id base
  String stt = String(g_mqttTopic) + "/" + id;         // state topic (the JSON)
  String cmd = stt + "/set_interval";                  // command topic (number -> interval)
  String avt = avtTopic();                             // per-gateway LWT: online/offline
  // device name: the user-set friendly name when present (Tasmota-style), else the technical default.
  // Safe to change later: entity ids/history hang off uniq_id + state topic, both stay untouched.
  String dev = "\"dev\":{\"ids\":[\"" + uid + "\"],\"name\":" +
               (r.name[0] ? jstr(r.name) : "\"OBI " + String(typeName(r.devType)) + " " + id + "\"") +
               ",\"mf\":\"OBI\",\"mdl\":\"" + String(typeName(r.devType)) +
               "\",\"sw\":\"" + String(r.softver) + "\",\"hw\":\"" + String(r.hardver) + "\"}";

  // --- sensors (dc/unit/sc == nullptr -> that field is omitted from the config) ---
  // import/export/power may be null in the JSON -> guarded so we never push a bogus 0.
  struct SDef { const char *key, *name, *dc, *unit, *sc, *tpl; bool diag; };
  static const SDef defs[] = {
    {"import",   "Import",    "energy",          "Wh",  "total_increasing",
       "{% if value_json['import'] is not none %}{{ value_json['import'] }}{% endif %}", false},
    {"export",   "Export",    "energy",          "Wh",  "total_increasing",
       "{% if value_json['export'] is not none %}{{ value_json['export'] }}{% endif %}", false},
    {"power",    "Power",     "power",           "W",   "measurement",
       "{% if value_json['power'] is not none %}{{ value_json['power'] }}{% endif %}", false},
    {"power_calc", "Power (calculated)", "power", "W",  "measurement",
       "{% if value_json['power_calc'] is not none %}{{ value_json['power_calc'] }}{% endif %}", false},
    {"battery",  "Battery",   "voltage",         "V",   "measurement",
       "{{ (value_json['battery_mV']|float(0))/1000 }}", true},
    {"rssi",     "RSSI",      "signal_strength", "dBm", "measurement",
       "{{ value_json['rssi'] }}", true},
    {"snr",      "SNR",       nullptr,           "dB",  "measurement",   // no signal_strength dc: that forces dBm; SNR is dB
       "{{ value_json['snr'] }}", true},
    {"lastseen", "Last seen", "duration",        "s",   "measurement",
       "{{ value_json['age_s'] }}", true},
    {"id",       "ID",        nullptr,           nullptr, nullptr,   // original 3-byte LoRa id; stays visible after a rename
       "{{ value_json['id'] }}", true},
    {"uuid",     "UUID",      nullptr,           nullptr, nullptr,
       "{% if value_json['uuid'] %}{{ value_json['uuid'] }}{% endif %}", true},
    {"type",     "Type",      nullptr,           nullptr, nullptr,
       "{{ value_json['type'] }}", true},
    {"firmware", "Firmware",  nullptr,           nullptr, nullptr,
       "{{ value_json['softver'] }}", true},
    {"hardware", "Hardware",  nullptr,           nullptr, nullptr,
       "{{ value_json['hardver'] }}", true},
  };
  for (const SDef &d : defs) {
    String topic = "homeassistant/sensor/" + uid + "/" + d.key + "/config";
    String p = "{\"name\":\"" + String(d.name) + "\",\"uniq_id\":\"" + uid + "_" + d.key + "\""
               ",\"stat_t\":\"" + stt + "\",\"val_tpl\":\"" + d.tpl + "\",\"avty_t\":\"" + avt + "\"";
    if (d.dc)   p += ",\"dev_cla\":\"" + String(d.dc) + "\"";
    if (d.unit) p += ",\"unit_of_meas\":\"" + String(d.unit) + "\"";
    if (d.sc)   p += ",\"stat_cla\":\"" + String(d.sc) + "\"";
    if (d.diag) p += ",\"ent_cat\":\"diagnostic\"";
    p += "," + dev + "}";
    mqtt.publish(topic.c_str(), p.c_str(), true);
  }

  // --- binary sensors (all diagnostic) ---
  struct BDef { const char *key, *name, *field; };
  static const BDef bdefs[] = {
    {"infrared",   "Optical sensor",  "infrared"},
    {"paired",     "Paired",          "paired"},
    {"legacy",     "Legacy protocol", "legacy"},
    {"bootloader", "Bootloader mode", "bootloader"},
  };
  for (const BDef &b : bdefs) {
    String topic = "homeassistant/binary_sensor/" + uid + "/" + b.key + "/config";
    String p = "{\"name\":\"" + String(b.name) + "\",\"uniq_id\":\"" + uid + "_" + b.key + "\""
               ",\"stat_t\":\"" + stt + "\",\"val_tpl\":\"{{ 'ON' if value_json['" + b.field + "'] else 'OFF' }}\""
               ",\"avty_t\":\"" + avt + "\",\"ent_cat\":\"diagnostic\"," + dev + "}";
    mqtt.publish(topic.c_str(), p.c_str(), true);
  }

  // --- number: upload interval (writes seconds to <base>/<id>/set_interval) ---
  String ntopic = "homeassistant/number/" + uid + "/interval/config";
  String np = "{\"name\":\"Upload interval\",\"uniq_id\":\"" + uid + "_interval\""
              ",\"stat_t\":\"" + stt + "\",\"cmd_t\":\"" + cmd + "\""
              ",\"val_tpl\":\"{% if value_json['interval'] %}{{ value_json['interval'] }}{% endif %}\""
              ",\"min\":1,\"max\":65535,\"step\":1,\"mode\":\"box\",\"unit_of_meas\":\"s\""
              ",\"avty_t\":\"" + avt + "\",\"ent_cat\":\"config\"," + dev + "}";
  mqtt.publish(ntopic.c_str(), np.c_str(), true);
}

