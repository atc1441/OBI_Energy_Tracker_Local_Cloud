// ============================ energy history ================================
// Persistent per-reader logging to LittleFS (the 128 KB "spiffs" partition of min_spiffs):
//   /s<id>  recent samples  "epoch,importWh,exportWh,powerW\n"  (ring buffer, capped ~8 KB)
//   /d<id>  daily summary    "dayStartEpoch,importWh,exportWh\n" (one row per LOCAL calendar day)
// The /history page draws the cumulative kWh lines from the samples and — the headline the user
// asked for — the per-day consumption from the kWh-COUNTER deltas (day[i].import - day[i-1].import),
// because the Watt reading is not always sent by the meter and can't be trusted; Watt is only tabled.
// All file I/O runs on the web task (core 0), never on the LoRa loop.
#define HIST_SLOTS 64                   // must stay >= MAX_READERS (defined in main.cpp; it's an extern const,
                                        // so it can't size a static array here)
static bool     g_fsOk = false;
static float    g_priceCent = 31.0f;    // GLOBAL electricity price in € cent per kWh (one device-wide setting,
                                        // not per reader; user-set, persisted to NVS namespace "obigw" key "pkwh")
static uint32_t hImp[HIST_SLOTS];       // last-logged import per reader slot
static uint32_t hLastMs[HIST_SLOTS];    // millis() of last sample append (0 = nothing logged yet)
static uint32_t hDayFlush[HIST_SLOTS];  // millis() of last daily-file write
static uint32_t hDay[HIST_SLOTS];       // local day-start of the last daily row written

// Mount LittleFS on whatever spare data partition this device actually has. The partition table is fixed
// at the original serial flash and is NOT changed by OTA, so we can't rely on a "spiffs" label — this
// build's boards ship a 128 KB "userdata" data partition instead. Try the known labels in order; the first
// data partition that exists gets LittleFS (formatted on first use). The WiFi/MQTT config lives in the
// separate "nvs" partition, so formatting this spare partition is safe.
static bool mountHistoryFS() {
  static const char *labels[] = {"userdata", "spiffs", "storage", "littlefs", "ffat"};
  for (const char *l : labels) {
    if (esp_partition_find_first(ESP_PARTITION_TYPE_DATA, ESP_PARTITION_SUBTYPE_ANY, l) &&
        LittleFS.begin(true, "/littlefs", 5, l)) {
      Serial.printf("[web] history: LittleFS on partition '%s'\n", l);
      return true;
    }
  }
  return false;
}

static bool timeValid() { return time(nullptr) > 1735689600; }   // wall clock past 2025-01-01 == NTP synced
static String fpS(const String &id) { return "/s" + id; }
static String fpD(const String &id) { return "/d" + id; }

static String fsRead(const String &path) {
  File f = LittleFS.open(path, "r");
  if (!f) return String();
  String s = f.readString();
  f.close();
  return s;
}
static bool fsWrite(const String &path, const String &data) {
  File f = LittleFS.open(path, "w");
  if (!f) return false;
  f.print(data);
  f.close();
  return true;
}

// Append one sample line, then keep the file bounded (drop the oldest lines past ~8 KB).
static void appendSample(const String &id, uint32_t ep, uint32_t imp, uint32_t exp, uint32_t pw) {
  String path = fpS(id);
  File f = LittleFS.open(path, "a");
  if (!f) return;
  char line[80];
  if (obi_na(pw)) snprintf(line, sizeof line, "%lu,%lu,%lu,\n",
                           (unsigned long)ep, (unsigned long)imp, (unsigned long)exp);
  else            snprintf(line, sizeof line, "%lu,%lu,%lu,%ld\n",   // power is signed (negative on feed-in)
                           (unsigned long)ep, (unsigned long)imp, (unsigned long)exp, (long)(int32_t)pw);
  f.print(line);
  size_t sz = f.size();
  f.close();
  if (sz > 8000) {                       // compact: keep only the newest ~5 KB (whole lines)
    String all = fsRead(path);
    int cut = (int)all.length() - 5000;
    int nl  = all.indexOf('\n', cut > 0 ? cut : 0);
    if (nl >= 0) fsWrite(path, all.substring(nl + 1));
  }
}

// Upsert the current local day's counter reading; days are monotonic so "today" is always the last row.
static void upsertDaily(const String &id, uint32_t dayStart, uint32_t imp, uint32_t exp) {
  String path = fpD(id);
  String all  = fsRead(path);
  char row[56];
  snprintf(row, sizeof row, "%lu,%lu,%lu", (unsigned long)dayStart, (unsigned long)imp, (unsigned long)exp);
  String content;
  if (all.length() == 0) {
    content = String(row) + "\n";
  } else {
    int end = (int)all.length();
    if (all[end - 1] == '\n') end--;                     // ignore trailing newline
    int start = all.lastIndexOf('\n', end - 1) + 1;      // start index of the last line (0 if single line)
    uint32_t lastDay = strtoul(all.substring(start, end).c_str(), nullptr, 10);
    if (lastDay == dayStart) content = all.substring(0, start) + row + "\n";   // replace today's row
    else {
      content = all;
      if (content.length() && content[content.length() - 1] != '\n') content += "\n";
      content += String(row) + "\n";                     // a new day -> append
    }
  }
  // cap to the most recent ~240 days
  int lines = 0; for (size_t k = 0; k < content.length(); k++) if (content[k] == '\n') lines++;
  if (lines > 240) {
    int drop = lines - 240, idx = 0;
    for (int d = 0; d < drop; d++) idx = content.indexOf('\n', idx) + 1;
    content = content.substring(idx);
  }
  fsWrite(path, content);
}

// Poll readers and log a sample when the counter moves or every 5 min (heartbeat). Throttled internally.
static void historyService() {
  if (!g_fsOk || !timeValid()) return;
  static uint32_t lastScan = 0;
  uint32_t now = millis();
  if (now - lastScan < 15000) return;                    // scan at most every 15 s
  lastScan = now;
  time_t tnow = time(nullptr);
  struct tm lt; localtime_r(&tnow, &lt);
  lt.tm_hour = 0; lt.tm_min = 0; lt.tm_sec = 0;
  uint32_t dayStart = (uint32_t)mktime(&lt);              // local midnight (Europe/Berlin) as epoch
  for (int i = 0; i < MAX_READERS; i++) {
    Reader &r = readers[i];
    if (!r.used || !r.haveData || obi_na(r.import_)) continue;
    bool firstLog  = (hLastMs[i] == 0);
    bool changed   = (r.import_ != hImp[i]);
    bool heartbeat = (now - hLastMs[i] >= 5UL * 60 * 1000);
    if (!(firstLog || changed || heartbeat)) continue;
    if (!firstLog && (now - hLastMs[i] < 30000)) continue;   // min 30 s spacing between samples
    String id = hex(r.handle, 3);
    appendSample(id, (uint32_t)tnow, r.import_, r.export_, r.power);
    if (firstLog || changed || dayStart != hDay[i] || now - hDayFlush[i] >= 10UL * 60 * 1000) {
      upsertDaily(id, dayStart, r.import_, obi_na(r.export_) ? 0 : r.export_);
      hDayFlush[i] = now; hDay[i] = dayStart;
    }
    hImp[i] = r.import_; hLastMs[i] = now;
  }
}

// Stream a CSV blob as a JSON array-of-arrays (empty field -> null), chunked to keep RAM low.
static void streamRows(const String &csv) {
  int i = 0, n = (int)csv.length(); bool firstRow = true;
  String chunk; chunk.reserve(1600);
  while (i < n) {
    int nl = csv.indexOf('\n', i); if (nl < 0) nl = n;
    String line = csv.substring(i, nl); line.trim(); i = nl + 1;
    if (!line.length()) continue;
    String row = firstRow ? "[" : ",["; firstRow = false;
    int fs = 0, col = 0;
    for (int k = 0; k <= (int)line.length(); k++) {
      if (k == (int)line.length() || line[k] == ',') {
        String f = line.substring(fs, k); fs = k + 1;
        if (col) row += ",";
        row += f.length() ? f : String("null");
        col++;
      }
    }
    row += "]";
    chunk += row;
    if (chunk.length() > 1400) { server.sendContent(chunk); chunk = ""; }
  }
  if (chunk.length()) server.sendContent(chunk);
}

